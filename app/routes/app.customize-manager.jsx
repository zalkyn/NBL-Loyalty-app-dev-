// ── Loader ────────────────────────────────────────────────────────────────────

export const loader = async ({ request }) => {
    const { session } = await authenticate.admin(request);

    const style = await prisma.style.findUnique({
        where: { shop: session.shop },
    });

    const STYLE_KEYS = Object.keys(stylePresets);

    // Build schema from DB if it exists, otherwise fall back to stylePresets.
    const base = style ?? stylePresets;
    const schema = {};
    for (const key of STYLE_KEYS) {
        schema[key] = base[key] ?? stylePresets[key] ?? {};
    }

    // Detect mismatch: keys or fields in DB that are not in stylePresets (or vice versa).
    let syncWarning = null;
    if (style) {
        const dbKeys = Object.keys(style).filter((k) => STYLE_KEYS.includes(k));
        const missingInDb = STYLE_KEYS.filter((k) => !(k in style) || style[k] === null);
        const extraInDb = dbKeys.filter((k) => {
            const dbTab = style[k];
            const presetTab = stylePresets[k];
            if (!presetTab || !dbTab) return false;
            // Check if DB tab has fields not present in preset.
            const dbFields = Object.keys(dbTab);
            const presetFields = Object.keys(presetTab);
            const extra = dbFields.filter((f) => !(f in presetTab));
            const missing = presetFields.filter((f) => !(f in dbTab));
            return extra.length > 0 || missing.length > 0;
        });

        if (missingInDb.length > 0 || extraInDb.length > 0) {
            syncWarning = {
                missingInDb,
                mismatchedTabs: extraInDb,
            };
        }
    }

    return { schema, syncWarning };
};


// ── Action ────────────────────────────────────────────────────────────────────

export const action = async ({ request }) => {
    const { session, admin } = await authenticate.admin(request);
    const formData = await request.formData();
    const intent = formData.get("intent");

    if (intent === "save") {
        const raw = formData.get("schema");

        // Parse error — malformed JSON from client.
        let schema;
        try {
            schema = JSON.parse(raw);
        } catch (err) {
            return {
                ok: false,
                errorType: "parse",
                message: "Failed to parse schema data.",
                detail: err.message,
            };
        }

        // DB save error.
        try {
            await prisma.style.upsert({
                where: { shop: session.shop },
                update: schema,
                create: { shop: session.shop, sessionId: session.id, ...schema },
            });
        } catch (err) {
            console.error("[StyleManager] DB upsert failed:", err);
            return {
                ok: false,
                errorType: "db",
                message: "Failed to save to database.",
                detail: err.message,
            };
        }

        // Preset file sync — non-fatal, DB save already succeeded.
        let presetSynced = true;
        let presetError = null;
        try {
            const { writeFile } = await import("fs/promises");
            const { resolve } = await import("path");
            const presetsPath = resolve("app/presets/stylePresets.js");
            const fileContent = `// Auto-generated by StyleManager — do not edit manually.\nexport const stylePresets = ${JSON.stringify(schema, null, 4)};\n`;
            await writeFile(presetsPath, fileContent, "utf8");
        } catch (err) {
            console.error("[StyleManager] Failed to write stylePresets.js:", err);
            presetSynced = false;
            presetError = err.message;
        }

        await syncAppConfig(admin, session);

        return {
            ok: true,
            savedSchema: schema,
            message: presetSynced
                ? "Schema saved and presets updated."
                : "Schema saved. Preset file sync failed — check server logs.",
            presetError: presetError ?? null,
        };
    }

    return {
        ok: false,
        errorType: "unknown_intent",
        message: `Unknown intent: "${intent}".`,
        detail: null,
    };
};


